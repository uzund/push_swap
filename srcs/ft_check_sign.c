#include "push_swap.h"

int	ft_check_sign(const char *s)
{
	int		i;
	int		count;
	char	c;

	count = 0;
	i = 0;
	while (s[i] != '\0')
	{
		c = s[i];
		if ((c != ' ' && c != '-' && !(c >= '0' && c <= '9')) && \
			(c != ' ' && c != '+' && !(c >= '0' && c <= '9')))
			return (0);
		if (((c == '-' && !(s[i + 1] >= '0' && s[i + 1] <= '9')) || \
			(i > 0 && c == '-' && !(s[i - 1] == ' '))) || \
			((c == '+' && !(s[i + 1] >= '0' && s[i + 1] <= '9')) || \
				(i > 0 && c == '+' && !(s[i - 1] == ' '))))
			return (0);
		if (c >= '0' && c <= '9')
			count++;
		i++;
	}
	if (count == 0)
		return (0);
	return (1);
}
