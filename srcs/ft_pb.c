#include "push_swap.h"

void	ft_pb(int *a, int *b, int *len_a, int *len_b)
{
	int	i;

	if (*len_a > 0)
	{
		*len_b += 1;
		b[*len_b - 1] = a[0];
		*len_a -= 1;
		i = 0;
		while (i < *len_a)
		{
			a[i] = a[i + 1];
			i++;
		}
	}
}
