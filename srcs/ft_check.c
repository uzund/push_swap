#include "push_swap.h"

int	ft_check(const char *s)
{
	int	i;
	int	k;

	k = 0;
	i = 0;
	while (s[i])
	{
		if ((s[i] == '-' || s[i] == '+') && k == 0)
			k++;
		else if (k > 1)
			return (0);
		else if ((s[i] >= '0' && s[i] <= '9') || !ft_isin(s[i]))
			i++;
		else
			return (0);
	}
	return (1);
}
