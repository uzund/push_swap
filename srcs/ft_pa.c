#include "push_swap.h"

void	pa(int *a, int *b, int *len_a, int *len_b)
{
	int	i;

	if (*len_b > 0)
	{
		*len_a += 1;
		i = *len_a;
		while (i > 0)
		{
			a[i] = a[i - 1];
			i--;
		}
		a[0] = b[0];
		*len_b -= 1;
		i = 0;
		while (i < *len_b)
		{
			b[i] = b[i + 1];
			i++;
		}
	}
}
